public class ExemploArray {

    /*
     * Array (Estático):
     * Um array estático é uma coleção de elementos do mesmo tipo, com tamanho fixo
     * definido durante a declaração.
     * 
     * Exemplo de declaração e uso de um array estático em Java:
     */

    public static void main(String[] args) {
        int[] meuArray = new int[5]; // Cria um array de inteiros com 5 elementos
        meuArray[0] = 10; // Atribui o valor 10 à primeira posição
        meuArray[1] = 20; // Atribui o valor 20 à segunda posição

    }
}
